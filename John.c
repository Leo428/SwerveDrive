//*!!Code automatically generated by 'ROBOTC' configuration wizard               !!*//

#define PID_DRIVE_MAX 127
#define PID_DRIVE_MIN -127

// magic numbers from tons of experiment
float  pid_Kp = 0.5;
float  pid_Ki = 0.0001;
float  pid_Kd = 0.5;

static bool pidRunning = true;
static float pidRequestedValue = 0.0;
//static float pidRequestedValueL = 0.0;

void clearAllEnc()
{
	resetMotorEncoder(motor[fLCtrl]);
	resetMotorEncoder(motor[bLCtrl]);
	resetMotorEncoder(motor[fRCtrl]);
	resetMotorEncoder(motor[bRCtrl]);
}

task pidControllerFL()
{
	float  pidSensorCurrentValue;
	float  pidError;
	float  pidLastError;

	float  pidIntegral;
	float  pidDerivative;
	float  pidDrive;
	//clear all encoders !
	clearAllEnc();
	//init
	pidLastError = 0;
	pidIntegral = 0;

	//looping >_<
	while(true)
	{
		if(pidRunning)
		{
			pidSensorCurrentValue = SensorValue[I2C_4];
			pidError =  pidRequestedValue - pidSensorCurrentValue;
			// important here above

			if(pid_Ki != 0)
			{
				// shall we have a limit???
				if(abs(pidError) < 50)
					pidIntegral = pidIntegral + pidError;
				else
					pidIntegral = 0;
			}
			else
				pidIntegral = 0;

			// derivative
			pidDerivative = pidError - pidLastError;
			pidLastError = pidError;

			// outputs
			pidDrive = (pid_Kp * pidError) + (pid_Ki * pidIntegral) + (pid_Kd * pidDerivative);

			// limit drive
			if(pidDrive > PID_DRIVE_MAX ) pidDrive = PID_DRIVE_MAX;
			else if(pidDrive < PID_DRIVE_MIN ) pidDrive = PID_DRIVE_MIN;

			motor[fLCtrl] = pidDrive;
		}
		else
		{
			pidError      = 0;
			pidLastError  = 0;
			pidIntegral   = 0;
			pidDerivative = 0;
			motor[fLCtrl] = 0;
		}
		wait1Msec(25);
	}
}

