		motor[fRCtrl] = -vexRT[Ch1];
		//motor[bLCtrl] = vexRT[Ch4];

		pidRequestedValue = SensorValue[I2C_1]; // master right
		//pidRequestedValueL = SensorValue[I2C_3]; // master left

